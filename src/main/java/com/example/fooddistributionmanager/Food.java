package com.example.fooddistributionmanager;

public class Food {
    int year;
    int month;
    String division;
    double rice;
    double soybeanOil;
    double onion;
    double potato;

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }
    public void setMonth(int month) {
        this.month = month;
    }

    public String getDivision() {
        return division;
    }

    public void setDivision(String division) {
        this.division = division;
    }

    public double getRice() {
        return rice;
    }

    public void setRice(double rice) {
        this.rice = rice;
    }

    public double getSoybeanOil() {
        return soybeanOil;
    }

    public void setSoybeanOil(double soybeanOil) {
        this.soybeanOil = soybeanOil;
    }

    public double getOnion() {
        return onion;
    }

    public void setOnion(double onion) {
        this.onion = onion;
    }

    public double getPotato() {
        return potato;
    }

    public void setPotato(double potato) {
        this.potato = potato;
    }
}
